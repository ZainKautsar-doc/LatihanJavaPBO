public class Matematika{
	public int pertambahan (int a, int b){		//metode pertambahan
		return a + b;
	}
	public int pengurangan (int a, int b){		//metode pengurangan
		return a - b;
	}
	public int perkalian (int a, int b){		//metode perkalian
		return a * b;
	}
	public int pembagian (int a, int b){		//metode untuk pembagian dan kalo menggunakan int akan langsung desimalnya dihilangkan
		return a / b ;
	}
}
